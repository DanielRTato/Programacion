public class Ej01 {
    public static void main(String[] args) {

        double resultado1 = ((3 + 2) % 2 - 15) / 2.0 * 5;
        System.out.println("Resultado de la primera operacion a) : " + resultado1);

        double resultado2 = (6 + (6.0 / 7)) + 35 / 2.0 - 8 * 5 / 4.0 * 2;
        System.out.println("El resultado de la segunda operacion b) es: " + resultado2);

        int resultado3 = 3 + 6 * 14 % 3;
        System.out.println("El resultado de la tercera operacion c) es: " + resultado3);

        double resultado4 = 8 + 7 * 3 + 4 * 6 / 2.0 % 4;
        System.out.println("El resultado de la cuarta operacion d) es: " + resultado4);

        double resultado5 = 27 % 4 + 15 / 4.0;
        System.out.println("El resultado de la quinta operacion e) es: " + resultado5);

        double resultado6 = 37 / (4.0 * 4) - 2;
        System.out.println("El resultado de la sexta operacion f) es: " + resultado6);

        double resultado7 = 9 * 2 / 3.0 * 25 * 3;
        System.out.println("El resultado de la septima operacion g) es: " + resultado7);

        double resultado8 = (7 * 3 - 4 * 4) * 2 / 4.0 * 2;
        System.out.println("El resultado de la octava operacion h) es: " + resultado8);
    }
}